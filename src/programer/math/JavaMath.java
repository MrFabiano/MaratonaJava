package programer.math;

//Realizando calculos com Math - Matematica
public class JavaMath {

    public static void main(String[] args) {

        System.out.println(Math.PI);

        System.out.println(Math.sin(45));

        System.out.println(Math.cos(45));

        System.out.println(Math.round(Math.PI)); //arredonda

        System.out.println(Math.ceil(Math.PI));

        System.out.println(Math.floor(Math.PI));

        System.out.println(Math.pow(3, 2));

        System.out.println(Math.sqrt(25)); // raiz quadraa
    }
}
