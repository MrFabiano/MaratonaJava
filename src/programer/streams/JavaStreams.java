package programer.streams;

public class JavaStreams {

    private String nome;
    private int alunos;

    public String getNome() {
        return nome;
    }

    public int getAlunos() {
        return alunos;
    }

    public JavaStreams(String nome, int alunos) {
        this.nome = nome;
        this.alunos = alunos;
    }
}
